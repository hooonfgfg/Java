package ch06homework;

public class Printer2 {

	static void println(boolean print) {
		System.out.println(print);
	}
	static void println(int print) {
		System.out.println(print);
	}
	static void println(double print) {
		System.out.println(print);
	}
	static void println(String print) {
		System.out.println(print);
	}
}
