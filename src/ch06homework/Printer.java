package ch06homework;

public class Printer {
	
	void println(boolean print) {
		System.out.println(print);
	}
	void println(int print) {
		System.out.println(print);
	}
	void println(double print) {
		System.out.println(print);
	}
	void println(String print) {
		System.out.println(print);
	}
}
